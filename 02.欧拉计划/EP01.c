#include<stdio.h>

/*
*欧拉计划01：1000以内3或5的倍数之和
* */

int main() {
    int sum3, sum5, sum15;
    sum3 = (3 + 999) * (999 / 3) / 2;
    sum5 = (5 + 995) * (995 / 5) / 2;
    sum15 = (15 + (1000 / 15) * 15) * (1000 / 15) / 2;
    printf("sum = %d\n", sum3 + sum5 - sum15);
    return 0;
}
